package controlrpc_test

import (
	"crypto/ed25519"
	"crypto/rand"
	"crypto/x509"
	"encoding/pem"
	"os"
	"path/filepath"
	"testing"
	"time"

	"github.com/Wide-Moat/ocu-control/internal/cred"
	"github.com/Wide-Moat/ocu-control/internal/state"
)

// newControlSigner loads a real cred.Signer over a freshly generated Ed25519 key
// on the given clock, so a dial mints a genuine per-dial exec JWT. The dial only
// reaches the wire if the mint succeeds, so a real Signer keeps the integration
// test end-to-end rather than stubbing the custody seam.
func newControlSigner(t *testing.T, clk state.Clock) *cred.Signer {
	t.Helper()
	_, priv, err := ed25519.GenerateKey(rand.Reader)
	if err != nil {
		t.Fatalf("generate ed25519: %v", err)
	}
	der, err := x509.MarshalPKCS8PrivateKey(priv)
	if err != nil {
		t.Fatalf("marshal pkcs8: %v", err)
	}
	pemBytes := pem.EncodeToMemory(&pem.Block{Type: "PRIVATE KEY", Bytes: der})
	path := filepath.Join(t.TempDir(), "exec-signing.key")
	if err := os.WriteFile(path, pemBytes, 0o600); err != nil {
		t.Fatalf("write key mount: %v", err)
	}
	cfg := cred.Config{
		Alg:             cred.AlgEdDSA,
		StorageIssuer:   "https://control.example/provisional",
		StorageAudience: "egress.provisional",
		ExecIssuer:      "https://control.example/exec-provisional",
		ExecAudience:    "guest.exec.provisional",
		StorageTTL:      10 * time.Minute,
	}
	signer, err := cred.LoadSignerFromMount(path, clk, cfg)
	if err != nil {
		t.Fatalf("LoadSignerFromMount: %v", err)
	}
	return signer
}
